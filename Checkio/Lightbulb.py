from datetime import datetime
from typing import List, Optional


def sum_light(els: List[datetime],
              start_watching: Optional[datetime] = None,
              end_watching: Optional[datetime] = None) -> int:
    total_sec = 0
    # return end_watching if end_watching is not None and end_watching > els[-1] else els[-1]
    # return els[0::2], els[1::2]+[max((end_watching if end_watching is not None else els[-1]), els[-1])]
    for dates in list(zip(els[0::2],
                          els[1::2]+[end_watching
                                     if end_watching is not None  and end_watching > els[-1]
                                     else els[-1]])):
        if start_watching is None or start_watching <= dates[0]:
            if end_watching is None or end_watching >= dates[1]:
                total_sec += (dates[1] - dates[0]).total_seconds()
            elif dates[0] < end_watching < dates[1]:
                total_sec += (end_watching - dates[0]).total_seconds()
        elif dates[0] < start_watching < dates[1]:
            if end_watching is None or end_watching >= dates[1]:
                total_sec += (dates[1] - start_watching).total_seconds()
            elif start_watching < end_watching < dates[1]:
                total_sec += (end_watching - start_watching).total_seconds()
    return total_sec


